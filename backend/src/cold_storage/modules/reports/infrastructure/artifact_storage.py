    # ------------------------------------------------------------------

    def put_temp(self, data: bytes, file_name: str) -> tuple[str, str]:
        """Write data to a temp file and return (temp_path, sha256).

        The temp file is created in the base_dir for easy cleanup.
        SHA-256 is computed and returned alongside the path.

        Parameters
        ----------
        data:
            Raw file bytes to store.
        file_name:
            Original file name (used for temp file suffix).

        Returns
        -------
        tuple[str, str]
            (temp_file_path, sha256_hex_digest)

        Raises
        ------
        ValueError
            If data is empty.
        """
        if not data:
            raise ValueError("Artifact data must not be empty")

        safe_name = _sanitize_file_name(file_name)
        # Create a unique temp file in the base directory
        fd, temp_path = tempfile.mkstemp(
            suffix=f"_{safe_name}",
            prefix="art_",
            dir=str(self._base_dir),
        )
        try:
            os.write(fd, data)
        finally:
            os.close(fd)

        # Verify SHA-256
        sha256_hash = self._compute_sha256(Path(temp_path))

        return temp_path, sha256_hash

    def finalize_temp(
        self,
        temp_path: str,
        artifact_id: str,
        file_name: str,
        *,
        claim_token: str = "",
        claim_version: int = 0,
    ) -> str:
        """Move temp file to final artifact location and write sidecar metadata.

        Uses :meth:`_publish_bundle` to atomically publish the payload and
        sidecar, ensuring that a crash never leaves an unowned artifact.

        Parameters
        ----------
        temp_path:
            Path to the temp file (from put_temp).
        artifact_id:
            The artifact UUID (used for directory structure).
        file_name:
            Original file name (will be sanitized).
        claim_token:
            Token of the owning claim (stored in sidecar metadata).
        claim_version:
            Version of the owning claim.

        Returns
        -------
        str
            Storage key (UUID-based) for later retrieval.

        Raises
        ------
        FileNotFoundError
            If the temp file does not exist.
        ValueError
            If the temp file is empty.
        """
        temp = Path(temp_path)
        if not temp.is_file():
            raise FileNotFoundError(f"Temp file not found: {temp_path}")

        file_size = temp.stat().st_size
        if file_size == 0:
            raise ValueError("Temp file is empty")

        storage_key = str(uuid.uuid4())
        safe_name = _sanitize_file_name(file_name)
        artifact_dir = self._base_dir / artifact_id
        artifact_dir.mkdir(parents=True, exist_ok=True)
        final_path = artifact_dir / f"{storage_key}_{safe_name}"

        # Read temp file bytes
        data = temp.read_bytes()

        # Atomically publish using the shared bundle primitive
        self._publish_bundle(
            data,
            artifact_id=artifact_id,
            storage_key=storage_key,
            final_path=final_path,
            claim_token=claim_token,
            claim_version=claim_version,
        )

        # Clean up the temp file now that we've published successfully
        temp.unlink(missing_ok=True)

        return storage_key

    def delete_legacy_artifact(
        self,
        storage_key: str,
        *,
        migration_actor: str,
        audit_reason: str,
        repository: Any,  # REQUIRED -- no default, no fallback to _write_audit_log
    ) -> None:
        """Privileged migration delete for legacy artifacts without owner metadata.

        Only callable by a migration/audit path, not by normal render/reclaim.
        Requires an explicit actor identity and reason for audit trail.

        Uses a transactional outbox pattern to ensure the audit record is
        committed BEFORE the file deletion:

        1. Insert ``DeletionOutboxRecord`` (status ``pending_audit``)
        2. ``repository.commit()`` -- audit is durable before file delete
        3. Perform crash-safe file deletion (quarantine + rename)
        4. Update outbox status to ``audited``
        5. ``repository.commit()`` again
        6. If file deletion fails: update status to ``delete_failed``,
           commit, then re-raise the exception.

        Parameters
        ----------
        storage_key:
            The UUID-based storage key.
        migration_actor:
            Identity of the user/process performing the migration delete.
        audit_reason:
            Reason for the privileged delete.
        repository:
            Repository instance used for outbox persistence.  REQUIRED.

        Raises
        ------
        ValueError
            If *migration_actor* or *audit_reason* are blank (or whitespace only).
        FileNotFoundError
            If the artifact does not exist.
        PermissionError
            If the file HAS a sidecar (use normal delete instead).
        """
        # Validate actor and reason are non-blank
        if not (migration_actor.strip() and audit_reason.strip()):
            raise ValueError("migration_actor and audit_reason must be non-empty strings")

        # Use direct file discovery (not get_path) because legacy files
        # lack sidecars, and get_path now validates sidecar existence.
        _validate_storage_key(storage_key)
        artifact_re = re.compile(r"^" + re.escape(storage_key) + r"_[A-Za-z0-9._-]+$")

        file_path: Path | None = None
        for artifact_dir in self._base_dir.iterdir():
            if artifact_dir.is_dir():
                for file in artifact_dir.iterdir():
                    if not file.is_file():
                        continue
                    name = file.name
                    if name.endswith(
                        (
                            ".meta",
                            ".meta.tmp",
                            ".reclaim_tmp",
                            ".pending_delete",
                            ".meta.pending_delete",
                            ".tmp",
                            ".partial",
                            ".backup",
                            ".meta.backup",
                            ".quarantine",
                        )
                    ):
                        continue
                    if artifact_re.match(name):
                        file_path = file.resolve()
                        break
                if file_path is not None:
                    break

        if file_path is None or not file_path.is_file():
            raise FileNotFoundError(f"Artifact not found: {storage_key}")

        meta_path = ReportArtifactStorage._meta_path(file_path)
        meta = ReportArtifactStorage._read_sidecar(meta_path)
        if meta is not None:
            raise PermissionError(
                f"Storage key {storage_key} has owner metadata. "
                f"Use delete() with correct claim_token/version instead."
            )

        # Compute source hash for audit trail
        source_hash = self._compute_sha256(file_path) if file_path.is_file() else ""

        # Step 1: Insert outbox (status='pending_audit')
        outbox_id = repository.insert_deletion_outbox(
            storage_key=storage_key,
            migration_actor=migration_actor,
            audit_reason=audit_reason,
            operation="legacy_delete",
            source_hash=source_hash,
        )

        # Step 2: Commit -- audit committed before file delete
        repository.commit()

        # Step 3: Perform file deletion (quarantine+rename pattern)
        try:
            pending_file = ReportArtifactStorage._pending_delete_path(file_path)
            file_path.rename(pending_file)
            pending_file.unlink(missing_ok=True)
        except Exception:
            # Step 5 (failure path): update status to delete_failed, commit, then raise
            repository.update_deletion_outbox_status(outbox_id, "delete_failed")
            repository.commit()
            raise

        # Step 4: Update outbox status to 'audited' on success
        repository.update_deletion_outbox_status(outbox_id, "audited")

        # Step 5: Commit again
        repository.commit()

    def _write_audit_log(
        self,
        *,
        storage_key: str,
        migration_actor: str,
        audit_reason: str,
        operation: str = "legacy_delete",
        result: str,
        source_hash: str | None = None,
    ) -> None:
        """Write a persistent audit log entry as a JSON file.

        Audit records are stored in ``<base_dir>/.audit_logs/<uuid>.json``
        so they survive filesystem reboots and are independent of any
        database connection.
        """
        audit_dir = self._base_dir / ".audit_logs"
        audit_dir.mkdir(parents=True, exist_ok=True)
        entry = {
            "id": str(uuid.uuid4()),
            "storage_key": storage_key,
            "migration_actor": migration_actor,
            "audit_reason": audit_reason,
            "operation": operation,
            "result": result,
            "source_hash": source_hash or "",
            "created_at": datetime.now(UTC).isoformat(),
        }
        audit_path = audit_dir / f"{entry['id']}.json"
        audit_path.write_text(json.dumps(entry, sort_keys=True, ensure_ascii=False))

    def cleanup_temp(self, temp_path: str) -> None:
        """Delete a temp file (best-effort, no error if missing).

        Parameters
        ----------
        temp_path:
            Path to the temp file to delete.
        """
        import contextlib

        with contextlib.suppress(Exception):
            Path(temp_path).unlink(missing_ok=True)
